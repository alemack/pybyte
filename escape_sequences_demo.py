# Новая строка:
print("Line 1\nLine 2")

# Табуляция:
print("Column A\tColumn B")

# Кавычки внутри строки:
print('He said: \'Hello\'')
print("She replied: \"Hi!\"")

# Обратный слэш:
print("Path on Windows: C:\\Users\\QA")

# Юникод символы:
print("Greek letters: \u03B1, \u03B2, \u03B3")

# «Сырые» строки (raw) — игнорируют escape-последовательности:
print(r"Raw string: здесь \n не перевод строки, а два символа '\' и 'n'")
