# 1) Простое использование eval:
# eval вычисляет одно выражение и возвращает результат.
expr = "2 + 3 * 4"
print(f"Eval: '{expr}' ->", eval(expr)) # -> 14

# 2) Простое использование exec:
# exec выполняет произвольный (многострочный) код, но не возвращает значение.
code = """
print("\\nExec: запускаем цикл из exec")
for i in range(1, 4):
    print(f" Шаг {i}")
"""
exec(code)